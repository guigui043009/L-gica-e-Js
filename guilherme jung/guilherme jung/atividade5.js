var nota1 = prompt("Nota: peso 4");

var nota2 = prompt("Nota: peso 8");

var nota3 = prompt("Nota: peso 10");



var resu1 = parseInt(nota1) * 4;

var resu2 = parseInt(nota2) * 8;

var resu3 = parseInt(nota3) * 10;



var mediaFinal = (resu1 + resu2 + resu3) / 3;



document.write("Sua média final é: 6", mediaFinal)